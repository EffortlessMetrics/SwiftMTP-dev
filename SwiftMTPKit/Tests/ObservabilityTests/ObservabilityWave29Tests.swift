// MARK: - Structured Log Metadata Propagation Tests

final class Wave29StructuredMetadataPropagationTests: XCTestCase {

  private func makeRecord(
    txID: UInt32 = 1, opcode: UInt16 = 0x1009, sessionID: UInt32 = 1,
    bytesIn: Int = 256, bytesOut: Int = 0, duration: TimeInterval = 0.1,
    outcome: TransactionOutcome = .ok, errorDescription: String? = nil
  ) -> TransactionRecord {
    TransactionRecord(
      txID: txID, opcode: opcode,
      opcodeLabel: MTPOpcodeLabel.label(for: opcode),
      sessionID: sessionID, startedAt: Date(timeIntervalSince1970: 1_700_000_000),
      duration: duration, bytesIn: bytesIn, bytesOut: bytesOut,
      outcomeClass: outcome, errorDescription: errorDescription)
  }

  func testMetadataPropagatesThroughAppendAndRecent() async {
    let log = TransactionLog()
    let original = makeRecord(txID: 77, opcode: 0x100D, sessionID: 9, bytesOut: 4096, duration: 2.5)
    await log.append(original)
    let retrieved = await log.recent(limit: 1)
    XCTAssertEqual(retrieved.count, 1)
    let r = retrieved[0]
    XCTAssertEqual(r.txID, 77)
    XCTAssertEqual(r.opcode, 0x100D)
    XCTAssertEqual(r.opcodeLabel, "SendObject")
    XCTAssertEqual(r.sessionID, 9)
    XCTAssertEqual(r.bytesOut, 4096)
    XCTAssertEqual(r.duration, 2.5, accuracy: 0.001)
  }

  func testMetadataPropagatesThroughJSONDumpRoundTrip() async throws {
    let log = TransactionLog()
    let original = makeRecord(
      txID: 42, opcode: 0x1008, sessionID: 3,
      bytesIn: 8192, duration: 0.75, outcome: .timeout,
      errorDescription: "timed out")
    await log.append(original)
    let json = await log.dump(redacting: false)
    let data = json.data(using: .utf8)!
    let decoder = JSONDecoder()
    decoder.dateDecodingStrategy = .iso8601
    let records = try decoder.decode([TransactionRecord].self, from: data)
    let r = try XCTUnwrap(records.first)
    XCTAssertEqual(r.txID, 42)
    XCTAssertEqual(r.opcode, 0x1008)
    XCTAssertEqual(r.opcodeLabel, "GetObjectInfo")
    XCTAssertEqual(r.sessionID, 3)
    XCTAssertEqual(r.bytesIn, 8192)
    XCTAssertEqual(r.outcomeClass, .timeout)
    XCTAssertEqual(r.errorDescription, "timed out")
  }

  func testOpcodeLabelAutoGeneratedFromOpcode() {
    let r = makeRecord(opcode: 0x1004)
    XCTAssertEqual(r.opcodeLabel, "GetStorageIDs")
  }

  func testOpcodeLabelUnknownAutoGenerated() {
    let r = makeRecord(opcode: 0xABCD)
    XCTAssertEqual(r.opcodeLabel, "Unknown(0xABCD)")
  }
}

// MARK: - Performance Metric Collection During Concurrent Operations

final class Wave29ConcurrentMetricCollectionTests: XCTestCase {

  func testConcurrentAppendsThenAggregateByteCounters() async {
    let log = TransactionLog()
    let count: UInt32 = 200
    await withTaskGroup(of: Void.self) { group in
      for i: UInt32 in 1...count {
        group.addTask {
          let record = TransactionRecord(
            txID: i, opcode: 0x1009, opcodeLabel: "GetObject",
            sessionID: 1, startedAt: Date(), duration: 0.01,
            bytesIn: 100, bytesOut: 50, outcomeClass: .ok)
          await log.append(record)
        }
      }
    }
    let all = await log.recent(limit: Int(count) + 10)
    XCTAssertEqual(all.count, Int(count))
    let totalBytesIn = all.reduce(0) { $0 + $1.bytesIn }
    let totalBytesOut = all.reduce(0) { $0 + $1.bytesOut }
    XCTAssertEqual(totalBytesIn, Int(count) * 100)
    XCTAssertEqual(totalBytesOut, Int(count) * 50)
  }

  func testConcurrentAppendsThenAggregateDurations() async {
    let log = TransactionLog()
    let count: UInt32 = 150
    await withTaskGroup(of: Void.self) { group in
      for i: UInt32 in 1...count {
        group.addTask {
          let record = TransactionRecord(
            txID: i, opcode: 0x1002, opcodeLabel: "OpenSession",
            sessionID: 1, startedAt: Date(), duration: 0.25,
            bytesIn: 0, bytesOut: 0, outcomeClass: .ok)
          await log.append(record)
        }
      }
    }
    let all = await log.recent(limit: 200)
    let totalDuration = all.reduce(0.0) { $0 + $1.duration }
    XCTAssertEqual(totalDuration, Double(count) * 0.25, accuracy: 0.001)
  }

  func testConcurrentEWMAUpdatesOnSeparateInstances() async {
    // Each task gets its own EWMA — verify no cross-contamination.
    let results = await withTaskGroup(of: Double.self, returning: [Double].self) { group in
      for rate in [1000.0, 2000.0, 5000.0, 10000.0] {
        group.addTask {
          var ewma = ThroughputEWMA()
          for _ in 0..<50 {
            ewma.update(bytes: Int(rate), dt: 1.0)
          }
          return ewma.bytesPerSecond
        }
      }
      var collected: [Double] = []
      for await value in group { collected.append(value) }
      return collected
    }
    XCTAssertEqual(results.count, 4)
    let sorted = results.sorted()
    XCTAssertEqual(sorted[0], 1000.0, accuracy: 1.0)
    XCTAssertEqual(sorted[1], 2000.0, accuracy: 1.0)
    XCTAssertEqual(sorted[2], 5000.0, accuracy: 1.0)
    XCTAssertEqual(sorted[3], 10000.0, accuracy: 1.0)
  }

  func testConcurrentRingBufferCollectionOnSeparateInstances() async {
    let results = await withTaskGroup(of: Double.self, returning: [Double].self) { group in
      for baseVal in [10.0, 100.0, 1000.0] {
        group.addTask {
          var buf = ThroughputRingBuffer(maxSamples: 50)
          for i in 0..<50 { buf.addSample(baseVal + Double(i)) }
          return buf.average ?? 0
        }
      }
      var collected: [Double] = []
      for await v in group { collected.append(v) }
      return collected
    }
    XCTAssertEqual(results.count, 3)
    let sorted = results.sorted()
    // Averages: 10+24.5=34.5, 100+24.5=124.5, 1000+24.5=1024.5
    XCTAssertEqual(sorted[0], 34.5, accuracy: 0.1)
    XCTAssertEqual(sorted[1], 124.5, accuracy: 0.1)
    XCTAssertEqual(sorted[2], 1024.5, accuracy: 0.1)
  }
}

// MARK: - Log Sink Configuration and Output Format Validation

final class Wave29LogSinkFormatTests: XCTestCase {

  private func makeRecord(txID: UInt32, errorDescription: String? = nil) -> TransactionRecord {
    TransactionRecord(
      txID: txID, opcode: 0x1009, opcodeLabel: "GetObject",
      sessionID: 1, startedAt: Date(timeIntervalSince1970: 1_700_000_000),
      duration: 0.5, bytesIn: 1024, bytesOut: 0,
      outcomeClass: .ok, errorDescription: errorDescription)
  }

  func testDumpOutputIsValidUTF8() async {
    let log = TransactionLog()
    await log.append(makeRecord(txID: 1))
    let json = await log.dump(redacting: false)
    XCTAssertNotNil(json.data(using: .utf8))
  }

  func testDumpOutputIsDecodableBackToRecords() async throws {
    let log = TransactionLog()
    for i: UInt32 in 1...3 { await log.append(makeRecord(txID: i)) }
    let json = await log.dump(redacting: false)
    let data = json.data(using: .utf8)!
    let decoder = JSONDecoder()
    decoder.dateDecodingStrategy = .iso8601
    let records = try decoder.decode([TransactionRecord].self, from: data)
    XCTAssertEqual(records.count, 3)
    XCTAssertEqual(records.map(\.txID), [1, 2, 3])
  }

  func testDumpSortedKeysOrder() async {
    let log = TransactionLog()
    await log.append(makeRecord(txID: 1, errorDescription: "err"))
    let json = await log.dump(redacting: false)
    // With sortedKeys: bytesIn < bytesOut < duration < errorDescription < opcode < ...
    let keys = ["bytesIn", "bytesOut", "duration", "errorDescription", "opcode",
                "opcodeLabel", "outcomeClass", "sessionID", "startedAt", "txID"]
    var lastIndex = json.startIndex
    for key in keys {
      if let range = json.range(of: "\"\(key)\"") {
        XCTAssertGreaterThanOrEqual(range.lowerBound, lastIndex, "Key \(key) out of sorted order")
        lastIndex = range.lowerBound
      }
    }
  }

  func testDumpRedactedOutputIsAlsoDecodable() async throws {
    let log = TransactionLog()
    await log.append(makeRecord(txID: 1, errorDescription: "serial=AABBCCDDEE112233"))
    let json = await log.dump(redacting: true)
    let data = json.data(using: .utf8)!
    let decoder = JSONDecoder()
