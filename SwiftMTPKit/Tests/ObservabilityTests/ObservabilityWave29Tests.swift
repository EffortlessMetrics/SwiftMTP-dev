  func testRingBufferAverageAccuracyWithLargeDataset() {
    var buf = ThroughputRingBuffer(maxSamples: 1000)
    var expectedSum = 0.0
    for i in 1...1000 {
      let v = Double(i)
      buf.addSample(v)
      expectedSum += v
    }
    let expectedAvg = expectedSum / 1000.0
