    await log.append(makeRecord(txID: 6, outcome: .ioError))
    let all = await log.recent(limit: 10)
    let okCount = all.filter { $0.outcomeClass == .ok }.count
    let errorCount = all.filter { $0.outcomeClass != .ok }.count
    XCTAssertEqual(okCount, 3)
    XCTAssertEqual(errorCount, 3)
  }

