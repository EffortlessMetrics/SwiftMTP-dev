// MARK: - Performance Regression Detection Thresholds

final class Wave29PerformanceRegressionThresholdTests: XCTestCase {

  func testDetectRegressionAboveThreshold() {
    var ewma = ThroughputEWMA()
    let baseline = 10_000_000.0
    // Simulate 50% regression
    for _ in 0..<50 { ewma.update(bytes: 5_000_000, dt: 1.0) }
    let regressionPct = (baseline - ewma.bytesPerSecond) / baseline * 100
    XCTAssertGreaterThan(regressionPct, 45.0)
  }

  func testNoRegressionWithinTolerance() {
    var ewma = ThroughputEWMA()
    let baseline = 10_000_000.0
    let tolerance = 5.0 // 5%
    // Simulate very close to baseline
    for _ in 0..<50 { ewma.update(bytes: 9_800_000, dt: 1.0) }
    let deviationPct = abs(baseline - ewma.bytesPerSecond) / baseline * 100
    XCTAssertLessThan(deviationPct, tolerance)
  }

  func testDetectImprovementAboveThreshold() {
    var ewma = ThroughputEWMA()
    let baseline = 5_000_000.0
    for _ in 0..<50 { ewma.update(bytes: 10_000_000, dt: 1.0) }
    let improvementPct = (ewma.bytesPerSecond - baseline) / baseline * 100
    XCTAssertGreaterThan(improvementPct, 90.0)
  }

  func testP95RegressionDetectionViaRingBuffer() {
    let baseline_p95 = 50.0
    var buf = ThroughputRingBuffer(maxSamples: 200)
    // Simulate slower p95
    for _ in 0..<95 { buf.addSample(30.0) }
    for _ in 0..<5 { buf.addSample(200.0) } // tail latency spike
    let current_p95 = buf.p95!
    XCTAssertGreaterThan(current_p95, baseline_p95)
  }

  func testStablePerformanceNoFalseRegressionAlarm() {
    var ewma = ThroughputEWMA()
    let baseline = 8_000_000.0
    let threshold = 10.0 // 10%
    // Feed stable rate
    for _ in 0..<100 { ewma.update(bytes: 8_000_000, dt: 1.0) }
    let deviationPct = abs(ewma.bytesPerSecond - baseline) / baseline * 100
    XCTAssertLessThan(deviationPct, threshold)
  }

  func testRegressionDetectedAfterGradualSlowdown() {
    var ewma = ThroughputEWMA()
    // Establish fast baseline
    for _ in 0..<30 { ewma.update(bytes: 10_000_000, dt: 1.0) }
    let fast = ewma.bytesPerSecond
    // Gradually slow down
    for _ in 0..<30 { ewma.update(bytes: 3_000_000, dt: 1.0) }
    let slow = ewma.bytesPerSecond
    XCTAssertLessThan(slow, fast * 0.5)
  }
}

